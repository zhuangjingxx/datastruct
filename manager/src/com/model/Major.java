package com.model;
//专业id,院id,专业名称
public class Major {

	private String maj_code;
	private String maj_collegeId;
	private String maj_name;
	public String getMaj_code() {
		return maj_code;
	}
	public void setMaj_code(String maj_code) {
		this.maj_code = maj_code;
	}
	public String getMaj_collegeId() {
		return maj_collegeId;
	}
	public void setMaj_collegeId(String maj_collegeId) {
		this.maj_collegeId = maj_collegeId;
	}
	public String getMaj_name() {
		return maj_name;
	}
	public void setMaj_name(String maj_name) {
		this.maj_name = maj_name;
	}
	
	
	
	
}
